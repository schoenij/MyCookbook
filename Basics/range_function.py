for i in range(5):
    print(i)    # Starts at 0 and does not include 5

for i in range(5, 10):  # Starts at 5 and does not include 10
    print(i)

for i in range(0, 10, 3):   # Starts at 0 goes to 10 and increases by increments of 3
    print(i)

for i in range(0, -10, -2):     # Counts backwards in increments of 2 from 0 to -10
    print(i)
